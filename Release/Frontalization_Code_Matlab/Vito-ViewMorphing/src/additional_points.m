function pts = additional_points(pts,varargin)

if nargin > 1
    img = varargin{1};
end

if size(pts,2) == 2
    pts = [
           pts;...
           round(mean(pts([1,18],1))),  round(mean(pts(37:42,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.4);...
           round(mean(pts(37:42,1))),   round(mean(pts(37:42,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.7);...
           round(mean(pts(43:48,1))),   round(mean(pts(43:48,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.7);...
           round(mean(pts([17,27],1))), round(mean(pts(43:48,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.4);...
          ];
elseif size(pts,2) == 3
    pts = [
           pts;...
           round(mean(pts([1,18],1))),  round(mean(pts(37:42,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.4), img(round(mean(pts(37:42,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.4), round(mean(pts([1,18],1))));...
           round(mean(pts(37:42,1))),   round(mean(pts(37:42,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.7), img(round(mean(pts(37:42,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.7), round(mean(pts(37:42,1))));...
           round(mean(pts(43:48,1))),   round(mean(pts(43:48,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.7), img(round(mean(pts(43:48,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.7), round(mean(pts(43:48,1))));...
           round(mean(pts([17,27],1))), round(mean(pts(43:48,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.4), img(round(mean(pts(43:48,2))-(mean(pts(49:end,2))-mean(pts(43:48,2)))*.4), round(mean(pts([17,27],1))));...
          ];
end

pts(pts<1) = 1;
if nargin > 1
    pts(pts(:,1)>size(img,2),1) = size(img,2);
    pts(pts(:,2)>size(img,1),2) = size(img,1);
end
